public class TypeCasting_5 {
    public static void main(String[] args) {
        long number=125;
        byte shorterNumber=(byte)number;
        float f=10.5f;
        int num=(int)f;
        System.out.println(shorterNumber);
        System.out.println(num);

        float eggs=12.5f;
        int wholePart=(int)eggs;
        System.out.println(wholePart);




    }
}
